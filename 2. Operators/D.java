
// 4. Relational Operators
import java.util.*;

public class D {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println(10 == 5);
        System.out.println(10 != 5);
        System.out.println(10 > 5);
        System.out.println(10 < 5);
        System.out.println(10 >= 5);
        System.out.println(10 <= 5);
        sc.close();
    }
}