
// 5. Logical Operators
import java.util.*;

public class E {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println((10 > 5) && (5 > 10));
        // System.out.println((10 > 5) || (5 > 10));
        System.out.println(!(10 > 5));
        sc.close();
    }
}