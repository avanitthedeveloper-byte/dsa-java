
// 11. Product of a & b
import java.util.*;

public class K {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter First & Second Number: ");
        float a = sc.nextFloat();
        float b = sc.nextFloat();
        System.out.println("Product of " + a + " & " + b + " : " + (a * b));
        sc.close();
    }
}